$string['modulename_help'] = 'El módulo de actividad de Foro Avanzado les permite a los participantes tener discusiones asincrónicas, que tienen lugar sobre un periododo extenso de tiempo.

Hay varios tipos de foros a elegir, tales como el foro estándar, donde cualquiera puede empezar una discusión en cualquier momento; un foro en donde cada estudiante puede publicar exactamente una discusión; o un foro de preguntas y respuestas en donde los estudiantes deben de publicar antes de poder ver las publicaciones de los otros estudiantes. Un maestro puede permitir que se anexen archivos a las publicaciones de los foros. Las imágenes anexas son mostradas en las publicaciones del foro.

Los participantes pueden suscribirse a un foro, para recibir notificaciones acerca de nuevas publicaciones. Un maestro puede configurar el modo de suscripción a opcional, forzado o auto, o impedir completamente la suscripción. Si se requiere, los estudiantes pueden ser censurados para que no publiquen más de un cierto número de publicaciones en un periodo de tiempo dado; esto puede impedir que algunos individuos acaparen las discusiones.

Las publicaciones en foros pueden ser valoradas por maestros o estudiantes (evaluación por pares). Las valoraciones pueden agregarse para formar una calificación final que se guarda en el libro de calificaciones.

Los foros tienen muchos usos, tales como:

* Un espacio social para que los estudiantes se conozcan
* Para anuncios del curso (usando un foro de noticias con suscripción forzosa)
* Para discutir contenidos del curso o materiales de lectura
* Para continuar en línea un asunto que surgió previamente en una sesión presencial
* Para discusiones sólo para maestros (usando un foro oculto)
* Un centro de ayuda, en donde los tutores y estudiantes pueden dar consejo
* Una área de apoyo uno-a-uno para comunicaciones privadas entre estudiante y maestro (usando un foro con grupos separados y con un estudiante por grupo)
* Para actividades de extensión, por ejemplo, retos mentales para que los estudiantes piensen y den sugerencias.
